
public class VarEx1 {

	public static void main(String[] args) {
		System.out.println("6+3"); 
		System.out.println(6+3); 
		System.out.println(6-3); 
		System.out.println(6*3); 
		System.out.println(6/3);
		
		int x=6,y=3;
		System.out.println(x+y );
		System.out.println(x-y );
		System.out.println(x*y );
		System.out.println(x/y );
		
	}

}
